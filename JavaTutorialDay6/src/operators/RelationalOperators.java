package operators;

public class RelationalOperators {

	public static void main(String[] args) {

		String var1 = "123456A", var2 = "123456";

		float num1 = 12.16515f, num2 = 12.16515f;

		System.out.println(var1 == var2);
		System.out.println(var1 != var2);

		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		System.out.println(num1 > num2);
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2);

	}

}
